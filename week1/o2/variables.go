package main

import "fmt"

func main() {
	// Variable declaration
	var name string = "Gopher"

	// Short declaration (type inferred)
	age := 5

	// Constants
	const pi = 3.14159

	// Basic types
	var isActive bool = true
	var height float64 = 1.7
	var count int = 10
	var letter byte = 'A'

	fmt.Println("Variables - Name:", name)
	fmt.Println("Variables - Age:", age)
	fmt.Println("Variables - Is active:", isActive)
	fmt.Println("Variables - Height:", height)
	fmt.Println("Variables - Count:", count)
	fmt.Println("Variables - Letter:", letter)
}
